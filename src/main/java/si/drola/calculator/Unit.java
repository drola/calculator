package si.drola.calculator;

/**
 * Unit definition
 * 
 */
public class Unit {
	protected String name;
	protected String value;
	
	/**
	 * Get unit name
	 * 
	 * @return Unit name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Set unit name
	 * 
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * Get unit value
	 * 
	 * @return Unit value
	 */
	public String getValue() {
		return value;
	}
	
	/**
	 * Set unit value
	 * 
	 * @param value
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
